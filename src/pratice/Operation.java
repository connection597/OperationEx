package pratice;

public class Operation {

	public static void main(String[] args) {
		int a =15 ,b=8;
		System.out.println("a +b=" +(a+b));
		System.out.println("a -b=" +(a-b));
		System.out.println("a *b=" +(a*b));
		System.out.println("a /b=" +(a/b));
		System.out.println("a %b=" +(a%b)); //나누기의 나머지
		
	}

}
